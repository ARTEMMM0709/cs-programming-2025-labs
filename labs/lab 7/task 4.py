# Создание списка словарей с информацией о зонах
# Каждый словарь содержит название зоны и время её активности (начало и конец)
zones = [
    {"zone": "Sector-12", "active_from": 8, "active_to": 18},     # Активна с 8 до 18
    {"zone": "Deep Storage", "active_from": 0, "active_to": 24},  # Активна круглосуточно
    {"zone": "Research Wing", "active_from": 9, "active_to": 17}  # Активна с 9 до 17
]

# Фильтрация зон: оставляем только те, которые работают строго с 8 до 18 часов
# Lambda-функция проверяет условие: начало активности >= 8 И конец активности <= 18
day_zones = list(filter(
    lambda zone: (zone["active_from"] >= 8) and (zone["active_to"] <= 18),
    zones  # Применяем фильтр к списку zones
))

# Вывод заголовка для пояснения результата
print("Зоны, работающие в дневной период с 8 до 18: ")

# Вывод отфильтрованного списка
# В результате останется только Sector-12 (8-18) и Research Wing (9-17)
print(day_zones)