# Создание списка длительностей смен охраны в часах
shifts = [6, 12, 8, 24, 10, 4]

# Фильтрация смен: оставляем только те, которые длятся от 8 до 12 часов включительно
# Lambda-функция проверяет условие: x >= 8 И x <= 12
valid_shifts = list(filter(lambda x: 8 <= x <= 12, shifts))

# Вывод поясняющего заголовка
print("Смены охраны, которые длятся от 8 до 12 часов включительно: ")

# Вывод отфильтрованного списка
# Результат: [12, 8, 10] (6 и 4 - слишком короткие, 24 - слишком длинная)
print(valid_shifts)