# Создание списка словарей с информацией об инцидентах
# Каждый словарь содержит ID инцидента и количество задействованного персонала
incidents = [
    {"id": 101, "staff": 4},   # Инцидент с 4 сотрудниками
    {"id": 102, "staff": 12},  # Инцидент с 12 сотрудниками
    {"id": 103, "staff": 7},   # Инцидент с 7 сотрудниками
    {"id": 104, "staff": 20}   # Инцидент с 20 сотрудниками
]

# Сортировка и выбор топ-3 инцидентов:
# 1. sorted() сортирует список incidents по ключу "staff" (количество персонала)
# 2. reverse=True - сортировка по убыванию (от большего к меньшему)
# 3. [:3] - срез, берет первые 3 элемента (самые ресурсоемкие инциденты)
top_incidents = sorted(incidents, key=lambda x: x["staff"], reverse=True)[:3]

# Вывод поясняющего заголовка
print("Самые ресурсоемкие инциденты: ")

# Вывод топ-3 инцидентов
# Результат: инциденты с ID 104 (20 сотрудников), 102 (12) и 103 (7)
print(top_incidents)